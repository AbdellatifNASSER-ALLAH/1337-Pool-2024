#include <unistd.h>

int	main(void)
{
	char	c;

	c = 'Z';
	while (c >= 'A')
	{
		if (c % 2 == 0)
		{
			c += 32;
			write(1, &c, 1);
			c -= 32;
		}
		else
		{
			write(1, &c, 1);
		}
		c--;
	}
	write(1, "\n", 1);
	return (0);
}
