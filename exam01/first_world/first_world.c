#include <unistd.h>

int	main(int ac, char *ar[])
{
	char* str;
	
	str = ar[1];
	if ( ac == 2)
	{
		while (*str == ' ' || *str == '\t')
			str++;
		while (*str != ' ' && *str != '\t' && *str)
			write(1, str++, 1);
	}
	write(1, "\n",1);
}
